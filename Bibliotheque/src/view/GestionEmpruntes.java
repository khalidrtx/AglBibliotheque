 private void Listing(){
       ArrayList<EmpruntBibliotheque> tab=E.getAll();
       DefaultTableModel dm = new DefaultTableModel(null,new String[]{"ID","Etudiant","Livre","Date Reservation","Date Retour"});
       dm.setNumRows(0);
       for(EmpruntBibliotheque r:tab){
           Object[] m={r.getId(),r.getEmprunteur().getPrenom()+' '+r.getEmprunteur().getNom(),r.getLivre().getTitre(),r.getDateEmprunt(),r.getDateRetour()};
           dm.addRow(m);
             }
       tableEm.setModel(dm);
        dm.fireTableDataChanged();
    
        }
